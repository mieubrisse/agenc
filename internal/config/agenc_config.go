// RepoConfig represents per-repo configuration in the repoConfig map.
// All fields are optional: alwaysSynced controls whether the server keeps
// the repo continuously fetched, emoji sets the display emoji for the repo,
// title sets a user-friendly display name, postUpdateHook specifies a shell
// command to run after repo updates, and writeableCopy designates a separate
// path the server keeps continuously synced with the same git remote
// (auto-commit local edits, pull-and-rebase remote changes). Setting
// writeableCopy implies alwaysSynced=true.
type RepoConfig struct {
	AlwaysSynced      bool               `yaml:"alwaysSynced,omitempty"`
	Emoji             string             `yaml:"emoji,omitempty"`
	Title             string             `yaml:"title,omitempty"`
	TrustedMcpServers *TrustedMcpServers `yaml:"trustedMcpServers,omitempty"`
	DefaultModel      string             `yaml:"defaultModel,omitempty"`
	PostUpdateHook    string             `yaml:"postUpdateHook,omitempty"`
	ClaudeArgs        []string           `yaml:"claudeArgs,omitempty"`
	WriteableCopy     string             `yaml:"writeableCopy,omitempty"`
}

// TrustedMcpServers configures MCP server trust for a repository.
// Supports two formats: "all" (trust every server in .mcp.json) or
// a list of named servers to trust.
type TrustedMcpServers struct {
	All  bool     // true when "all" is specified
	List []string // populated when a list of server names is specified
}

// UnmarshalYAML implements yaml.Unmarshaler to handle "all" (string)
// or a list of server names.
func (t *TrustedMcpServers) UnmarshalYAML(unmarshal func(interface{}) error) error {
	var str string
	if err := unmarshal(&str); err == nil {
		if str == "all" {
			t.All = true
			return nil
		}
		return fmt.Errorf("trustedMcpServers: invalid value %q; must be \"all\" or a list of server names", str)
	}

	var list []string
	if err := unmarshal(&list); err == nil {
		if len(list) == 0 {
			return fmt.Errorf("trustedMcpServers: empty list is not valid; use \"all\" to trust all servers, or list at least one server name")
		}
		t.List = list
		return nil
	}

	return fmt.Errorf("trustedMcpServers: must be \"all\" or a list of server names")
}

// MarshalYAML implements yaml.InterfaceMarshaler to serialize back to
// either the string "all" or a list of server names.
func (t TrustedMcpServers) MarshalYAML() (interface{}, error) {
	if t.All {
		return "all", nil
	}
	return t.List, nil
}

