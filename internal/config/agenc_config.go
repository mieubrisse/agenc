// GetAllSyncedRepos returns the sorted list of repo names that have alwaysSynced enabled.
func (c *AgencConfig) GetAllSyncedRepos() []string {
	var repos []string
	for repoName, rc := range c.RepoConfigs {
		if rc.AlwaysSynced {
			repos = append(repos, repoName)
		}
	}
	sort.Strings(repos)
	return repos
}

